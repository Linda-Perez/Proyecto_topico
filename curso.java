public class curso {
    String materia;
    String salon;
    String horario;
    String nombre;
    int codigo;
    public curso(){
    }
    public curso(String materia,String salon,String horario,String nombre,int codigo){
        this.materia = materia;
        this.salon = salon;
        this.horario = horario;
        this.nombre = nombre;
        this.codigo = codigo;
    }
}
